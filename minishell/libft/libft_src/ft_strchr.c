#include "../include/libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	int		size;
	char	*p;

	size = ft_strlen(s);
	p = (char *) s;
	i = 0;
	while (i <= size)
	{
		if (*p == (char) c)
			return (p);
		p ++;
		i ++;
	}
	return (0);
}
