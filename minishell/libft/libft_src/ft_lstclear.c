#include "../include/libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*ptr;
	t_list	*prev;

	ptr = *lst;
	prev = 0;
	if (!lst || !*lst || !del)
		return ;
	while (ptr)
	{
		del(ptr->content);
		prev = ptr;
		ptr = ptr->next;
		free(prev);
	}
	*lst = 0;
}
